// Terraform file generators for IBM Cloud, Oracle OCI, Alibaba Cloud, DigitalOcean, Linode.
// Each cloud returns Record<filename, content> matching the same shape as terraform-generator.ts.

export type AltTfCloud = "ibm" | "oci" | "alibaba" | "do" | "linode";

const DATE = new Date().toISOString().slice(0, 10);

// ── helpers ──────────────────────────────────────────────────────────────────

const banner = (cloud: string, file: string, desc: string) =>
  `# ${"═".repeat(60)}\n# ${file} — ${cloud} | ${desc}\n# Generated by InfraStudio · ${DATE}\n# ${"═".repeat(60)}\n`;

const has = (services: string[], ...ids: string[]) => ids.some(id => services.includes(id));

// ── IBM Cloud ─────────────────────────────────────────────────────────────────

function ibmFiles(services: string[], region: string): Record<string, string> {
  const files: Record<string, string> = {};

  files["main.tf"] = banner("IBM Cloud", "main.tf", "Provider & Terraform settings") + `
terraform {
  required_version = ">= 1.5"
  required_providers {
    ibm = {
      source  = "IBM-Cloud/ibm"
      version = "~> 1.65"
    }
  }
}

provider "ibm" {
  region = var.ibm_region
}

# HOW TO USE:
#   export IC_API_KEY="<your-ibm-cloud-api-key>"
#   terraform init
#   terraform plan  -var-file="dev.tfvars"
#   terraform apply -var-file="dev.tfvars"
#
# Docs: https://registry.terraform.io/providers/IBM-Cloud/ibm/latest/docs`;

  files["variables.tf"] = banner("IBM Cloud", "variables.tf", "Input variables") + `
variable "project_name"       { type = string; default = "myapp" }
variable "environment"        { type = string; default = "dev" }
variable "ibm_region"         { type = string; default = "${region}" }
variable "node_count"         { type = number; default = 2 }
variable "machine_type"       { type = string; default = "bx2.4x16"  }
variable "db_plan"            { type = string; default = "standard"   }
variable "storage_class"      { type = string; default = "standard"   }
variable "enable_monitoring"  { type = bool;   default = false        }
variable "log_retention_days" { type = number; default = 7            }
variable "multi_az"           { type = bool;   default = false        }`;

  files["networking.tf"] = banner("IBM Cloud", "networking.tf", "VPC, subnets, security groups") + `
resource "ibm_resource_group" "main" {
  name = "\${var.project_name}-\${var.environment}"
}

resource "ibm_is_vpc" "main" {
  name           = "\${var.project_name}-vpc-\${var.environment}"
  resource_group = ibm_resource_group.main.id
  tags           = [var.project_name, var.environment]
}

resource "ibm_is_public_gateway" "main" {
  name           = "\${var.project_name}-pgw-\${var.environment}"
  vpc            = ibm_is_vpc.main.id
  zone           = "\${var.ibm_region}-1"
  resource_group = ibm_resource_group.main.id
}

resource "ibm_is_subnet" "public" {
  name                     = "\${var.project_name}-public-\${var.environment}"
  vpc                      = ibm_is_vpc.main.id
  zone                     = "\${var.ibm_region}-1"
  total_ipv4_address_count = 256
  public_gateway           = ibm_is_public_gateway.main.id
  resource_group           = ibm_resource_group.main.id
}

resource "ibm_is_subnet" "private" {
  name                     = "\${var.project_name}-private-\${var.environment}"
  vpc                      = ibm_is_vpc.main.id
  zone                     = "\${var.ibm_region}-2"
  total_ipv4_address_count = 256
  resource_group           = ibm_resource_group.main.id
}

resource "ibm_is_security_group" "app" {
  name           = "\${var.project_name}-sg-\${var.environment}"
  vpc            = ibm_is_vpc.main.id
  resource_group = ibm_resource_group.main.id
}

resource "ibm_is_security_group_rule" "https_in" {
  group     = ibm_is_security_group.app.id
  direction = "inbound"
  remote    = "0.0.0.0/0"
  tcp { port_min = 443; port_max = 443 }
}`;

  if (has(services, "control-plane", "vm-instance", "serverless-compute")) {
    files["compute.tf"] = banner("IBM Cloud", "compute.tf", "IKS Kubernetes cluster") + `
resource "ibm_container_vpc_cluster" "main" {
  name              = "\${var.project_name}-iks-\${var.environment}"
  vpc_id            = ibm_is_vpc.main.id
  flavor            = var.machine_type
  worker_count      = var.node_count
  kube_version      = "1.29"
  resource_group_id = ibm_resource_group.main.id
  wait_till         = "IngressReady"

  zones {
    subnet_id = ibm_is_subnet.private.id
    name      = "\${var.ibm_region}-1"
  }

  tags = [var.project_name, var.environment]
}

data "ibm_container_cluster_config" "main" {
  cluster_name_id   = ibm_container_vpc_cluster.main.id
  resource_group_id = ibm_resource_group.main.id
}`;
  }

  if (has(services, "managed-database", "database")) {
    files["database.tf"] = banner("IBM Cloud", "database.tf", "IBM Databases for PostgreSQL") + `
resource "ibm_database" "main" {
  name              = "\${var.project_name}-db-\${var.environment}"
  plan              = var.db_plan
  location          = var.ibm_region
  service           = "databases-for-postgresql"
  resource_group_id = ibm_resource_group.main.id
  tags              = [var.project_name, var.environment]

  parameters_json = jsonencode({
    members_memory_allocation_mb = 2048
    members_disk_allocation_mb   = 20480
  })

  timeouts { create = "120m"; update = "60m"; delete = "15m" }
}

resource "ibm_resource_key" "db_key" {
  name                 = "\${var.project_name}-db-key-\${var.environment}"
  role                 = "Administrator"
  resource_instance_id = ibm_database.main.id
}`;
  }

  if (has(services, "block-storage", "object-storage", "file-storage")) {
    files["storage.tf"] = banner("IBM Cloud", "storage.tf", "Cloud Object Storage (COS)") + `
resource "ibm_resource_instance" "cos" {
  name              = "\${var.project_name}-cos-\${var.environment}"
  service           = "cloud-object-storage"
  plan              = "standard"
  location          = "global"
  resource_group_id = ibm_resource_group.main.id
  tags              = [var.project_name, var.environment]
}

resource "ibm_cos_bucket" "main" {
  bucket_name          = "\${var.project_name}-\${var.environment}-data"
  resource_instance_id = ibm_resource_instance.cos.id
  region_location      = var.ibm_region
  storage_class        = var.storage_class
}`;
  }

  if (has(services, "load-balancer")) {
    files["loadbalancer.tf"] = banner("IBM Cloud", "loadbalancer.tf", "Application Load Balancer") + `
resource "ibm_is_lb" "main" {
  name           = "\${var.project_name}-lb-\${var.environment}"
  subnets        = [ibm_is_subnet.public.id]
  type           = "public"
  resource_group = ibm_resource_group.main.id
  tags           = [var.project_name, var.environment]
}

resource "ibm_is_lb_listener" "https" {
  lb           = ibm_is_lb.main.id
  port         = 443
  protocol     = "https"
  default_pool = ibm_is_lb_pool.main.id
}

resource "ibm_is_lb_pool" "main" {
  lb                  = ibm_is_lb.main.id
  name                = "app-pool"
  protocol            = "http"
  algorithm           = "round_robin"
  health_delay        = 10
  health_retries      = 2
  health_timeout      = 5
  health_type         = "http"
  health_monitor_url  = "/health"
  health_monitor_port = 8080
}`;
  }

  files["outputs.tf"] = banner("IBM Cloud", "outputs.tf", "Resource identifiers & connection info") + `
output "resource_group_id" { value = ibm_resource_group.main.id }
output "vpc_id"             { value = ibm_is_vpc.main.id }
output "public_subnet_id"  { value = ibm_is_subnet.public.id }
output "private_subnet_id" { value = ibm_is_subnet.private.id }` +
    (has(services, "control-plane", "vm-instance") ? `
output "cluster_id"   { value = ibm_container_vpc_cluster.main.id }
output "cluster_name" { value = ibm_container_vpc_cluster.main.name }` : "") +
    (has(services, "managed-database", "database") ? `
output "db_id" { value = ibm_database.main.id; sensitive = true }` : "") +
    (has(services, "block-storage", "object-storage", "file-storage") ? `
output "cos_bucket" { value = ibm_cos_bucket.main.bucket_name }` : "");

  return files;
}

