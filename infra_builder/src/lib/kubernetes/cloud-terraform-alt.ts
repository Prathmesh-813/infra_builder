export function generateAltTerraformFiles(
  cloud: AltTfCloud,
  services: string[],
  region: string
): Record<string, string> {
  switch (cloud) {
    case "ibm":     return ibmFiles(services, region);
    case "oci":     return ociFiles(services, region);
    case "alibaba": return alibabaFiles(services, region);
    case "do":      return doFiles(services, region);
    case "linode":  return linodeFiles(services, region);
  }
}

export function generateAltTerraform(
  cloud: AltTfCloud,
  services: string[],
  region: string
): string {
  return Object.values(generateAltTerraformFiles(cloud, services, region)).join("\n\n");
}

