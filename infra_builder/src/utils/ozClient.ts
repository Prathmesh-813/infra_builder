export type AgentStatus = 'pending' | 'running' | 'completed' | 'failed' | 'cancelled' | 'paused';

export interface AgentSummary {
  id: number;
  agent_type: string;
  status: AgentStatus;
  started_at: string | null;
  finished_at: string | null;
  created_at: string;
  exit_code: number | null;
}
export interface AgentDetail extends AgentSummary {
  prompt: string | null;
  container_id: string | null;
  error: string | null;
}
export interface AgentLogLine { stream: string; content: string; timestamp: string; }

