function awsVariablesTf(nodes: Node<ResourceNodeData>[], region: string): string {
  const lines: string[] = [];

  lines.push(`# ============================================================
# variables.tf — AWS
# All environment-specific values are supplied via .tfvars files
# ============================================================

variable "environment" {
  description = "Deployment environment"
  type        = string

  validation {
    condition     = contains(["dev", "staging", "prod"], var.environment)
    error_message = "environment must be one of: dev, staging, prod."
  }
}

variable "aws_region" {
  description = "AWS region"
  type        = string
  default     = "${region}"
}

variable "aws_profile" {
  description = "AWS CLI profile"
  type        = string
  default     = "default"
}

variable "project_name" {
  description = "Project name used for resource naming and tagging"
  type        = string
}

variable "instance_type" {
  description = "EC2 / compute instance type"
  type        = string
}

variable "min_count" {
  description = "Minimum number of instances / nodes"
  type        = number
}

variable "max_count" {
  description = "Maximum number of instances / nodes"
  type        = number
}

variable "deletion_protection" {
  description = "Enable deletion protection on stateful resources"
  type        = bool
}

variable "high_availability" {
  description = "Enable multi-AZ / high-availability mode"
  type        = bool
}

variable "log_retention_days" {
  description = "CloudWatch log retention in days"
  type        = number
}

variable "backup_retention_days" {
  description = "Database backup retention in days"
  type        = number
}
`);

  // Resource-specific variables
  const seen = new Set<string>();
  for (const node of nodes) {
    const { resourceType, resourceName, config } = node.data;
    const safe = resourceName.replace(/[^a-zA-Z0-9_]/g, '_').toLowerCase();

    if (resourceType === 'aws_instance' && config.ami && !seen.has('ami_id')) {
      seen.add('ami_id');
      lines.push(`variable "ami_id" {
  description = "AMI ID for EC2 instances"
  type        = string
}
`);
    }

    if ((resourceType === 'aws_rds_instance' || resourceType === 'aws_rds_cluster') && !seen.has(`${safe}_db_password`)) {
      seen.add(`${safe}_db_password`);
      lines.push(`variable "${safe}_db_password" {
  description = "Master password for ${resourceName} database"
  type        = string
  sensitive   = true
}
`);
    }

    if (resourceType === 'aws_s3_bucket' && config.bucket && !seen.has(`${safe}_bucket_name`)) {
      seen.add(`${safe}_bucket_name`);
      lines.push(`variable "${safe}_bucket_name" {
  description = "S3 bucket name for ${resourceName}"
  type        = string
}
`);
    }

    if (resourceType === 'aws_vpc' && config.cidr_block && !seen.has(`${safe}_cidr`)) {
      seen.add(`${safe}_cidr`);
      lines.push(`variable "${safe}_cidr" {
  description = "CIDR block for ${resourceName} VPC"
  type        = string
}
`);
    }
  }

  return lines.join('\n');
}

function awsTfvars(env: Environment, nodes: Node<ResourceNodeData>[], region: string): string {
  const cfg = ENV_DEFAULTS[env];
  const lines: string[] = [header(env, 'AWS')];

  lines.push(`environment           = "${env}"`);
  lines.push(`aws_region            = "${region}"`);
  lines.push(`aws_profile           = "default"`);
  lines.push(`project_name          = "my-project-${env}"`);
  lines.push(`instance_type         = "${cfg.instanceSize}"`);
  lines.push(`min_count             = ${cfg.minCount}`);
  lines.push(`max_count             = ${cfg.maxCount}`);
  lines.push(`deletion_protection   = ${cfg.deletionProtection}`);
  lines.push(`high_availability     = ${cfg.highAvailability}`);
  lines.push(`log_retention_days    = ${cfg.logRetentionDays}`);
  lines.push(`backup_retention_days = ${cfg.backupRetentionDays}`);
  lines.push('');

  const seen = new Set<string>();
  for (const node of nodes) {
    const { resourceType, resourceName, config } = node.data;
    const safe = resourceName.replace(/[^a-zA-Z0-9_]/g, '_').toLowerCase();

    if (resourceType === 'aws_instance' && config.ami && !seen.has('ami_id')) {
      seen.add('ami_id');
      lines.push(`ami_id = "${config.ami}"`);
    }
    if ((resourceType === 'aws_rds_instance' || resourceType === 'aws_rds_cluster') && !seen.has(`${safe}_db_password`)) {
      seen.add(`${safe}_db_password`);
      const envSuffix = env === 'prod' ? 'CHANGE_ME_PROD' : env === 'staging' ? 'CHANGE_ME_STAGING' : 'CHANGE_ME_DEV';
      lines.push(`${safe}_db_password = "${envSuffix}"`);
    }
    if (resourceType === 'aws_s3_bucket' && config.bucket && !seen.has(`${safe}_bucket_name`)) {
      seen.add(`${safe}_bucket_name`);
      lines.push(`${safe}_bucket_name = "${config.bucket}-${env}"`);
    }
    if (resourceType === 'aws_vpc' && config.cidr_block && !seen.has(`${safe}_cidr`)) {
      seen.add(`${safe}_cidr`);
      // Shift CIDR second octet per env: dev=0, staging=1, prod=2
      const shift = env === 'dev' ? 0 : env === 'staging' ? 1 : 2;
      const cidr = String(config.cidr_block).replace(/^(\d+\.)(\d+)(\..*)$/, (_, a, b, c) => `${a}${Number(b) + shift}${c}`);
      lines.push(`${safe}_cidr = "${cidr}"`);
    }
  }

  return lines.join('\n') + '\n';
}

