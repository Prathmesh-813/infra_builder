function envReadme(provider: CloudProvider, region: string): string {
  const providerName = provider === 'aws' ? 'AWS' : provider === 'azure' ? 'Azure' : 'GCP';
  const varFileFlag = provider === 'azure' ? '-var-file="envs/dev.tfvars"' : '-var-file="envs/dev.tfvars"';

  return `# Terraform Infrastructure — ${providerName}

> Generated by [Terraform Visual Builder](https://github.com/your-repo)

## Environment-Based Approach

This project uses **per-environment \`.tfvars\` files** to manage configuration
across \`dev\`, \`staging\`, and \`prod\` without duplicating resource definitions.

\`\`\`
.
├── main.tf               # Resource definitions (shared across all envs)
├── providers.tf          # Provider + backend configuration
├── variables.tf          # Variable declarations (no defaults — set in tfvars)
├── outputs.tf            # Output values
└── envs/
    ├── dev.tfvars        # Development environment values
    ├── staging.tfvars    # Staging environment values
    └── prod.tfvars       # Production environment values
\`\`\`

## Quick Start

\`\`\`bash
# 1. Initialise
terraform init

# 2. Plan for a specific environment
terraform plan ${varFileFlag}

# 3. Apply
terraform apply ${varFileFlag}

# 4. Destroy
terraform destroy ${varFileFlag}
\`\`\`

## Environment Differences

| Variable              | dev          | staging      | prod              |
|-----------------------|--------------|--------------|-------------------|
| instance_type / size  | small        | medium       | large             |
| min_count             | 1            | 1            | 2                 |
| max_count             | 2            | 4            | 10                |
| deletion_protection   | false        | false        | true              |
| high_availability     | false        | false        | true              |
| log_retention_days    | 7            | 14           | 90                |
| backup_retention_days | 1            | 7            | 30                |

## Security Notes

- ⚠️  Never commit \`prod.tfvars\` with real credentials — use a secrets manager.
- ⚠️  Sensitive variables (passwords, subscription IDs) are marked \`sensitive = true\`.
- ✅  All resources are tagged/labelled with \`Environment\` and \`Project\`.
- ✅  Remote state backend config is included (commented out) in \`providers.tf\`.

## Region

Default region: **${region}**
`;
}

