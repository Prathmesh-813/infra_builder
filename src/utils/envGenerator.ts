export interface EnvTerraformProject {
  'main.tf': string;
  'providers.tf': string;
  'variables.tf': string;
  'outputs.tf': string;
  'envs/dev.tfvars': string;
  'envs/staging.tfvars': string;
  'envs/prod.tfvars': string;
  'README.md': string;
}

/**
 * Build a full environment-based Terraform project for any cloud provider.
 * The main.tf is taken from the existing single-file generators; the env
 * scaffolding (variables, providers, per-env tfvars) is generated here.
 */
export function buildEnvTerraformProject(
  provider: CloudProvider,
  nodes: Node<ResourceNodeData>[],
  region: string,
  mainTfContent: string,
  outputsTfContent: string,
): EnvTerraformProject {
  let variablesTf: string;
  let providersTf: string;
  let devTfvars: string;
  let stagingTfvars: string;
  let prodTfvars: string;

  switch (provider) {
    case 'aws':
      variablesTf  = awsVariablesTf(nodes, region);
      providersTf  = awsProvidersTf(region);
      devTfvars    = awsTfvars('dev',     nodes, region);
      stagingTfvars = awsTfvars('staging', nodes, region);
      prodTfvars   = awsTfvars('prod',    nodes, region);
      break;

    case 'azure':
      variablesTf  = azureVariablesTf();
      providersTf  = azureProvidersTf();
      devTfvars    = azureTfvars('dev');
      stagingTfvars = azureTfvars('staging');
      prodTfvars   = azureTfvars('prod');
      break;

    case 'gcp':
      variablesTf  = gcpVariablesTf(region);
      providersTf  = gcpProvidersTf(region);
      devTfvars    = gcpTfvars('dev',     region);
      stagingTfvars = gcpTfvars('staging', region);
      prodTfvars   = gcpTfvars('prod',    region);
      break;

    default:
      variablesTf  = '# No variable definitions for this provider\n';
      providersTf  = '# No provider configuration for this provider\n';
      devTfvars    = '# dev\n';
      stagingTfvars = '# staging\n';
      prodTfvars   = '# prod\n';
  }

  // Strip the inline terraform{} + provider{} + variable blocks from main.tf
  // so they don't conflict with the dedicated providers.tf / variables.tf
  const cleanMain = stripProviderBlock(mainTfContent);

  return {
    'main.tf':              cleanMain,
    'providers.tf':         providersTf,
    'variables.tf':         variablesTf,
    'outputs.tf':           outputsTfContent || '# No outputs defined\n',
    'envs/dev.tfvars':      devTfvars,
    'envs/staging.tfvars':  stagingTfvars,
    'envs/prod.tfvars':     prodTfvars,
    'README.md':            envReadme(provider, region),
  };
}

/**
 * Remove the leading terraform{}, provider{}, and top-level variable{} blocks
 * from a generated main.tf so they don't duplicate what's in providers.tf /
 * variables.tf.
 */
function stripProviderBlock(tf: string): string {
  const lines = tf.split('\n');
  const result: string[] = [];
  let depth = 0;
  let skipping = false;

  for (let i = 0; i < lines.length; i++) {
    const line = lines[i];
    const trimmed = line.trim();

    // Detect top-level blocks to skip
    if (depth === 0) {
      if (
        trimmed.startsWith('terraform {') ||
        trimmed.startsWith('provider ') ||
        trimmed.startsWith('variable "')
      ) {
        skipping = true;
      }
    }

    if (skipping) {
      // Track brace depth to find end of block
      for (const ch of line) {
        if (ch === '{') depth++;
        if (ch === '}') depth--;
      }
      if (depth === 0) {
        skipping = false;
        // Skip the blank line after the closing brace too
        if (i + 1 < lines.length && lines[i + 1].trim() === '') i++;
      }
      continue;
    }

    result.push(line);
  }

  // Trim leading blank lines
  while (result.length > 0 && result[0].trim() === '') result.shift();

  return result.join('\n');
}

export async function downloadEnvProjectAsZip(
  project: EnvTerraformProject,
  projectName = 'terraform-env-project',
): Promise<void> {
  const JSZip = (await import('jszip')).default;
  const zip = new JSZip();
  const folder = zip.folder(projectName)!;

  for (const [filename, content] of Object.entries(project)) {
    // JSZip handles nested paths (e.g. "envs/dev.tfvars") automatically
    folder.file(filename, content);
  }

  const blob = await zip.generateAsync({ type: 'blob' });
  const url = URL.createObjectURL(blob);
  const a = document.createElement('a');
  a.href = url;
  a.download = `${projectName}.zip`;
  a.click();
  URL.revokeObjectURL(url);
}
